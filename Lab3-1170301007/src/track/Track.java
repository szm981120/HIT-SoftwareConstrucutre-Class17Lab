package track;

public interface Track {

	/**
	 * Get radius of this track.
	 * 
	 * @return radius of this track
	 */
	public double getRadius();

	/**
	 * Get the long radius of this track.
	 * 
	 * @return long radius of this track
	 */
	public double getLongRadius();

	/**
	 * Get the short radius of this track.
	 * 
	 * @return short radius of this track
	 */
	public double getShortRadius();
}
